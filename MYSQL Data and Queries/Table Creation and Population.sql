create database new4;
use new4;

CREATE TABLE customer (
    CustomerID INT PRIMARY KEY,
    Fname VARCHAR(40) NOT NULL,
    Lname VARCHAR(40),
    PhoneNum CHAR(10),
    CONSTRAINT chk_phone CHECK (
		PhoneNum > 1000000000 and PhoneNum < 9999999999
    ),
    Email varchar(20),
    CONSTRAINT chk_email CHECK (
        Email LIKE '%@%._%'
    ),
    Pword VARCHAR(15) NOT NULL,
    CONSTRAINT chk_pword CHECK (
        length(Pword) >= 8
    )
); 

CREATE TABLE product (
    ProductID INT PRIMARY KEY,
    ProductName VARCHAR(50) NOT NULL,
    ProductType VARCHAR(50),
    PricePU DECIMAL(7,2) NOT NULL
);

CREATE TABLE store (
    StoreID INT PRIMARY KEY,
    Email VARCHAR(30) NOT NULL,
    CONSTRAINT chk_email1 CHECK (
        Email LIKE '%@%._%'
    ),
    PhoneNum CHAR(10),
    CONSTRAINT chk_phone1 CHECK (
		PhoneNum > 1000000000 and PhoneNum < 9999999999
    )
    Pword VARCHAR(15) NOT NULL,
    CONSTRAINT chk_pword1 CHECK (
        length(Pword) >= 8
    )
);

CREATE TABLE supplier (
    SupplierID INT PRIMARY KEY,
    SupplierName VARCHAR(30),
    PhoneNum CHAR(10),
    CONSTRAINT chk_phone2 CHECK (
		PhoneNum > 1000000000 and PhoneNum < 9999999999
    ),
    City VARCHAR(15) NOT NULL,
    Email VARCHAR(30) NOT NULL,
    CONSTRAINT chk_emai2l CHECK (
        Email LIKE '%@%._%'
    ),
    Pword VARCHAR(15) NOT NULL,
    CONSTRAINT chk_pword2 CHECK (
        length(Pword) >= 8
    )
);

CREATE TABLE admins (
    AdminID INT PRIMARY KEY,
    Pword VARCHAR(15) NOT NULL,
    CONSTRAINT chk_pword3 CHECK (
        length(Pword) >= 8
    )
);

CREATE TABLE employee (
    EmployeeID INT PRIMARY KEY,
    Fname VARCHAR(40) NOT NULL,
    Lname VARCHAR(40),
    PhoneNum CHAR(10),
    CONSTRAINT chk_phone4 CHECK (
		PhoneNum > 1000000000 and PhoneNum < 9999999999
    ),
    Salary DECIMAL(7,2) NOT NULL,
    Experience INT
);

CREATE TABLE deliveryAddress (
    CustomerID INT,
    AddressID INT,
    Pincode CHAR(6) NOT NULL,
    City VARCHAR(15),
    AddressLine1 VARCHAR(50),
    AddressLine2 VARCHAR(50),
    PRIMARY KEY(AddressID, CustomerID),
    FOREIGN KEY(CustomerID) references customer(CustomerID) ON DELETE CASCADE
);

CREATE TABLE storeAddress (
    AddressID INT,
    StoreID INT,
    Pincode CHAR(6) NOT NULL,
    City VARCHAR(15),
    AddressLine1 VARCHAR(50),
    AddressLine2 VARCHAR(50),
    PRIMARY KEY(AddressID, StoreID),
    FOREIGN KEY(StoreID) references store(StoreID) ON DELETE CASCADE
);

CREATE TABLE paymentMethod (
    PaymentID INT,
    CustomerID INT,
    paymentType VARCHAR(15),
    PRIMARY KEY(PaymentID, CustomerID),
    FOREIGN KEY(CustomerID) references customer(CustomerID) ON DELETE CASCADE
);

CREATE TABLE CustomerOrder (
    OrderID INT PRIMARY KEY,
    CustomerID INT,
    StoreID INT,
    ProductID INT,
    PaymentID INT,
    orderDate DATE NOT NULL,
    FOREIGN KEY(CustomerID) references customer(CustomerID) ON DELETE SET NULL,
    FOREIGN KEY(StoreID) references store(StoreID) ON DELETE SET NULL,
    FOREIGN KEY(ProductID) references product(ProductID) ON DELETE SET NULL,
    FOREIGN KEY(PaymentID) references paymentMethod(PaymentID) ON DELETE SET NULL
);

CREATE TABLE sells (
  StoreID INT NOT NULL,
  ProductID INT NOT NULL,
  Quantity INT,
  PRIMARY KEY(StoreID,ProductID),
  FOREIGN KEY(StoreID) references store(StoreID) ON DELETE CASCADE,
  FOREIGN KEY(ProductID) references product(ProductID) ON DELETE CASCADE
);

CREATE TABLE supplies (
    SupplierID INT NOT NULL,
    ProductID INT NOT NULL,
    CostPU DECIMAL(7,2) NOT NULL,
    PRIMARY KEY(SupplierID,ProductID),
    FOREIGN KEY(SupplierID) references supplier(SupplierID) ON DELETE CASCADE,
    FOREIGN KEY(ProductID) references product(ProductID) ON DELETE CASCADE
);

CREATE TABLE supplyQuotation (
    SupplyQuotationID INT PRIMARY KEY, 
    ProductID INT,
    StoreID INT, 
    SupplierID INT, 
    QuotationDate DATE NOT NULL,
    Quantity INT NOT NULL, 
    FOREIGN KEY(ProductID) references product(ProductID) ON DELETE SET NULL,
    FOREIGN KEY(StoreID) references store(StoreID) ON DELETE SET NULL,
    FOREIGN KEY(SupplierID) references supplier(SupplierID) ON DELETE SET NULL
);

CREATE TABLE Works (
    StoreID INT NOT NULL,
    EmployeeID INT NOT NULL,
    PRIMARY KEY(StoreID, EmployeeID), 
    FOREIGN KEY(StoreID) references store(StoreID) ON DELETE CASCADE,
    FOREIGN KEY(EmployeeID) references employee(EmployeeID) ON DELETE CASCADE
); 

CREATE TABLE Manages (
    StoreID INT UNIQUE,
    EmployeeID INT UNIQUE,
    DateofAppointment DATE NOT NULL,
    PRIMARY KEY(StoreID),
    FOREIGN KEY(StoreID) references store(StoreID) ON DELETE CASCADE,
    FOREIGN KEY(EmployeeID) references employee(EmployeeID) ON DELETE SET NULL
); 

CREATE TABLE Delivery (
    OrderID INT PRIMARY KEY,
    AddressID INT,
    DeliveryDate Date NOT NULL,
    FOREIGN KEY(OrderID) references CustomerOrder(OrderID) ON DELETE CASCADE,
    FOREIGN KEY(AddressID) references DeliveryAddress(AddressID) ON DELETE SET NULL
); 

CREATE TABLE SupplyDelivery (
    SupplyQuotationID INT PRIMARY KEY, 
    AddressID INT,  
    ReceivingDate Date NOT NULL,
    FOREIGN KEY(SupplyQuotationID) references SupplyQuotation(SupplyQuotationID) ON DELETE CASCADE,
    FOREIGN KEY(AddressID) references StoreAddress(AddressID) ON DELETE SET NULL
);

CREATE TABLE GenericLogin (
    loginID INT,
    LoginType VARCHAR (15),
    LogTime TIMESTAMP,
    PRIMARY KEY (loginID, LoginType, LogTime)
);

CREATE TABLE CustomerLogin (
    CustomerID INT,
    LogTime TIMESTAMP,
    PRIMARY KEY(CustomerID, LogTime),
    FOREIGN KEY(CustomerID) references customer(CustomerID) ON DELETE CASCADE
);


CREATE TABLE SupplierLogin (
    SupplierID INT,
    LogTime TIMESTAMP,
    PRIMARY KEY(SupplierID, LogTime),
    FOREIGN KEY(SupplierID) references supplier(SupplierID) ON DELETE CASCADE
);


CREATE TABLE StoreLogin (
    employeeID INT,
    LogTime TIMESTAMP,
    PRIMARY KEY(employeeID, LogTime),
    FOREIGN KEY(employeeID) references employee(employeeID) ON DELETE CASCADE
);

